package lu.pcy113.pdr.engine.utils.consts;

import org.lwjgl.opengl.GL40;

public enum DataType implements GLConstant {

	// Base type
	BYTE(GL40.GL_BYTE, null), SHORT(GL40.GL_SHORT, null), INT(GL40.GL_INT, null), HALF_FLOAT(GL40.GL_HALF_FLOAT, null), FLOAT(GL40.GL_FLOAT, null),

	// Unsigned base types
	UBYTE(GL40.GL_UNSIGNED_BYTE, BYTE), USHORT(GL40.GL_UNSIGNED_SHORT, SHORT), UINT(GL40.GL_UNSIGNED_INT, INT),

	UBYTE332(GL40.GL_UNSIGNED_BYTE_3_3_2, UBYTE), UBYTE233REV(GL40.GL_UNSIGNED_BYTE_2_3_3_REV, UBYTE), USHORT565(GL40.GL_UNSIGNED_SHORT_5_6_5, USHORT), USHORT565REV(GL40.GL_UNSIGNED_SHORT_5_6_5_REV, USHORT),
	USHORT4444(GL40.GL_UNSIGNED_SHORT_4_4_4_4, USHORT), USHORT4444REV(GL40.GL_UNSIGNED_SHORT_4_4_4_4_REV, USHORT), USHORT5551(GL40.GL_UNSIGNED_SHORT_5_5_5_1, USHORT), USHORT1555REV(GL40.GL_UNSIGNED_SHORT_1_5_5_5_REV, USHORT),
	UINT8888(GL40.GL_UNSIGNED_INT_8_8_8_8, UINT), UINT8888REV(GL40.GL_UNSIGNED_INT_8_8_8_8_REV, UINT), UINT1010102(GL40.GL_UNSIGNED_INT_10_10_10_2, UINT), UINT2101010REV(GL40.GL_UNSIGNED_INT_2_10_10_10_REV, UINT),
	UINT248(GL40.GL_UNSIGNED_INT_24_8, UINT), UINT10F11F11FREV(GL40.GL_UNSIGNED_INT_10F_11F_11F_REV, UINT), UINT5999REV(GL40.GL_UNSIGNED_INT_5_9_9_9_REV, UINT),

	FLOAT32UINT248REV(GL40.GL_FLOAT_32_UNSIGNED_INT_24_8_REV, null);

	private int glId;
	private DataType base;

	private DataType(int glId, DataType base) {
		this.glId = glId;
	}

	@Override
	public int getGlId() {
		return glId;
	}

	public DataType getBase() {
		return base;
	}

	public boolean isBase() {
		return base == null;
	}

}
